def add(x,y):
    z=x+y
    return z
def sub(a,b):
    c=a-b
    return c
def multi(e,f):
    g=e*f
    return g
def divide(m,n):
    o=m/n
    return o
def modulus(g,h):
    i=g%h
    return i
def exponent(r,s):
    t=r**s
    return t
def floor_division(u,v):
    d=u//v
    return d
def main():
    print(add(10,6))
    print(sub(5,3))
    print(multi(4,6))
    print(divide(10,2))
    print(modulus(5,2))
    print(exponent(2,2))
    print(floor_division(2,9))
main()